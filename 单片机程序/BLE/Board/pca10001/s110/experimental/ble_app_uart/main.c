//定义发送数据的缓冲区，这个功能是个转发数据的功能。所以程序上就不大改了，采用这样的策略去解决这个问题
//串口接收到数据后凑齐BLE_NUS_MAX_DATA_LEN就进行一次发送，同时每次接收到数据之后启动定时器（时间大于波特率）。
//当定时器超时后，即使没有收到足够BLE_NUS_MAX_DATA_LEN的数据也发送出去。
#define  UART_DELAY                 (2000U) 
#define  BLE_UART_REV_SIZE           2000
static uint8_t data_array[BLE_UART_REV_SIZE];                       //发送数据的缓冲区    
static volatile uint32_t index = 0;                                 //发送数据位置

