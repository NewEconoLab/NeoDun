void LED_Init(void)
{
  // Configure LED-pins as outputs
  nrf_gpio_cfg_output(LED_0);
  nrf_gpio_cfg_output(LED_1);
}

void Time2_init(void)  //
{
    NRF_TIMER2->MODE        = TIMER_MODE_MODE_Timer;          
		NRF_TIMER2->BITMODE = (TIMER_BITMODE_BITMODE_32Bit << TIMER_BITMODE_BITMODE_Pos); 	
		NRF_TIMER2->TASKS_COUNT = 0;
    NRF_TIMER2->PRESCALER   = 2;  
		NRF_TIMER2->TASKS_CLEAR = 1;
    NRF_TIMER2->CC[0]       = UART_DELAY;
    NRF_TIMER2->INTENSET    = TIMER_INTENSET_COMPARE0_Enabled << TIMER_INTENSET_COMPARE0_Pos;
		NRF_TIMER2->EVENTS_COMPARE[0] = 0;     //清除事件
    NRF_TIMER2->SHORTS      = (TIMER_SHORTS_COMPARE0_CLEAR_Enabled << TIMER_SHORTS_COMPARE0_CLEAR_Pos);
    NRF_TIMER2->TASKS_START = 1;        
    NVIC_ClearPendingIRQ(TIMER2_IRQn);
    NVIC_SetPriority(TIMER2_IRQn,3);
    NVIC_DisableIRQ(TIMER2_IRQn);        //默认不打开

}



void Time2_stop()
{
		NVIC_ClearPendingIRQ(TIMER2_IRQn);
		NRF_TIMER2->TASKS_CLEAR = 1;
		NRF_TIMER2->TASKS_STOP = 1;
		NRF_TIMER2->TASKS_START = 0;
		NVIC_DisableIRQ(TIMER2_IRQn);  
}

void Time2_reset()
{
	NVIC_ClearPendingIRQ(TIMER2_IRQn);
		NRF_TIMER2->TASKS_CLEAR = 1;
	NRF_TIMER2->TASKS_COUNT = 0;
	NRF_TIMER2->TASKS_STOP = 0;
	NRF_TIMER2->TASKS_START = 1;
 	NRF_TIMER2->EVENTS_COMPARE[0] = 0;     //清除事件
	//NRF_TIMER2->CC[0]       = 0;
	NVIC_EnableIRQ(TIMER2_IRQn);  
}


uint32_t  send_buffer(uint32_t tindex, uint8_t* pdata)
{
	int i = 0;
	uint32_t err_code;
	while (tindex > (BLE_NUS_MAX_DATA_LEN - 1))
		{
			err_code = ble_nus_send_string(&m_nus, pdata+i, BLE_NUS_MAX_DATA_LEN);
			if (err_code != NRF_ERROR_INVALID_STATE)
			{
					APP_ERROR_CHECK(err_code);
			}  
			tindex -= BLE_NUS_MAX_DATA_LEN;
			i += BLE_NUS_MAX_DATA_LEN;
		}
		if (tindex != 0)
		{
			err_code = ble_nus_send_string(&m_nus, pdata+i, tindex);
			if (err_code != NRF_ERROR_INVALID_STATE)
			{
					APP_ERROR_CHECK(err_code);
			}  
			index = 0;  
		}
	return err_code;
}



void TIMER2_IRQHandler(void)  
{
	uint32_t tindex = index;
	
	if (NRF_TIMER2->EVENTS_COMPARE[0] == 1)
	{ 
		NRF_TIMER2->EVENTS_COMPARE[0] = 0;     //清除事件
		Time2_stop();	
		send_buffer(tindex, data_array);
	}
	NVIC_ClearPendingIRQ(TIMER2_IRQn);
}

